const { user_roles } = require("../models");
const db = require("../models");
const Banners = db.banners;
const Op = db.Sequelize.Op;

// Create and Save a new Tutorial
exports.create = (req, res) => {
  // Validate request
  if (!req.body.banner_url) {
    res.status(400).send({
      message: "Content can not be empty!"
    });
    return;
  }

  // Create a Tutorial
  const banner = {
    banner_url: req.body.banner_url,
    lunch_url: req.body.lunch_url
  };
  if (req.is_admin == 1) {
    Banners.create(banner)
      .then(data => {
        res.send(data);
      })
      .catch(err => {
        res.status(500).send({
          message:
            err.message || "Some error occurred while adding the Banner."
        });
      });
  }
  // Save Tutorial in the database

};

// Retrieve all Tutorials from the database.
exports.findBanners = (req, res) => {

  var Sequelize = require('sequelize');
  Banners.findAll({ order: Sequelize.literal('rand()'), limit: 5 })
    .then(data => {
      res.send({ data });
    })
    .catch(err => {
      res.status(500).send({
        message:
          err.message || "Some error occurred while retrieving banners."
      });
    });
};

